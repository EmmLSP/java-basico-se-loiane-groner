package com.loiane.javabasico.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int val1 = 1;
		int val2 = 2;
		
		System.out.println("valor1 == valor2: " + (val1 == val2));
		System.out.println("valor1 != valor2: " + (val1 != val2));
		System.out.println("valor1 > valor2: " + (val1 > val2));
		System.out.println("valor1 >= valor2: " + (val1 >= val2));
		System.out.println("valor1 < valor2: " + (val1 < val2));
		System.out.println("valor1 <= valor2: " + (val1 <= val2));
	}

}
